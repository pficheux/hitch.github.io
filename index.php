<h2 style="line-height:150%;"><font face="Trebuchet MS"><span style="font-size:16pt;">&nbsp;&nbsp;Pourquoi ce site 
                         ?</span></font></h2>
<div align="justify">
                                <p style="line-height:150%;"><font face="Trebuchet MS"><span style="font-size:10pt;">&nbsp;&nbsp;Par pure admiration du père  Hitchcock. Pour tous les
moments de plaisir passés à voir et revoir ses films et bien sûr pour le
faire découvrir aux malheureux qui ignoreraient encore son oeuvre.<br>Je me souviens encore de la première fois que j'ai vu 
                        </span></font><A HREF="psychose.html"><font face="Trebuchet MS"><span style="font-size:10pt;">Psychose</span></font></A><font face="Trebuchet MS"><span style="font-size:10pt;"> au ciné-club de Claude-Jean Philippe il y a une quinzaine
d'années.<br> &nbsp;&nbsp;C'était très tard le soir
(après </span></font><EM><font face="Trebuchet MS"><span style="font-size:10pt;">Apostrophes</span></font></EM><font face="Trebuchet MS"><span style="font-size:10pt;">) et j'étais bien entendu mort de trouille mais 
persuadé d'avoir vu comme le dit </span></font><a href="biblio.html#spoto"><font face="Trebuchet MS"><span style="font-size:10pt;">Donald Spoto</span></font></a><font face="Trebuchet MS"><span style="font-size:10pt;">
                        </span></font><EM><font face="Trebuchet MS"><span style="font-size:10pt;">&quot;une oeuvre maîtresse de l'art contemporain&quot;</span></font></EM><font face="Trebuchet MS"><span style="font-size:10pt;">.<br>A &nbsp;la fin 
du film, je suis resté assis 
plusieurs minutes devant mon téléviseur éteint, sans bouger, en épiant tous 
les craquements de la maison.
Je l'ai revu plusieurs fois depuis et si le côté effrayant s'est estompé, 
l'émotion n'est jamais absente même en tapant ces quelques lignes...<br> &nbsp;Sans avoir la prétention de recréer l'émotion du maître, j'ai essayé de faire
des pages brèves en insistant sur le côté anecdotique et visuel de manière à
être complémentaire de l'excellente </span></font><A HREF="http://www.imdb.com/" target="_blank"
><font face="Trebuchet MS"><span style="font-size:10pt;">base de donnée du cinéma</span></font></A><font face="Trebuchet MS"><span style="font-size:10pt;">. Ce site est
en perpétuel  développement, j'ajouterai les fiches cinématographiques au 
fur et à mesure. Bien entendu, toutes les suggestions ou informations sont
les bienvenues. La page de 
                        </span></font><A HREF="biblio.html"><font face="Trebuchet MS"><span style="font-size:10pt;">bibliographie</span></font></A><font face="Trebuchet MS"><span style="font-size:10pt;">	 et la page concernant la
